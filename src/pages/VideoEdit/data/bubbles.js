export const bubblesByCategory = bubbles.reduce((acc, bubble) => {
  if (bubble.categories && bubble.categories.length > 0) {
    bubble.categories.forEach(category => {
      if (!acc[category.name]) {
        acc[category.name] = [];
      }
      acc[category.name].push(bubble);
    });
  } else {
    if (!acc['未分类']) {
      acc['未分类'] = [];
    }
    acc['未分类'].push(bubble);
  }
  return acc;
}, {});

