  // 按类别组织背景素材
  export const backgroundsByCategory = backgrounds.reduce((acc, bg) => {
    if (bg.categories && bg.categories.length > 0) {
      bg.categories.forEach(category => {
        if (!acc[category.name]) {
          acc[category.name] = [];
        }
        // 为每个背景添加预览图，如果没有cover，则使用url作为预览
        const bgWithCover = {
          ...bg,
          cover: bg.cover || bg.url,
          name: bg.name || `背景${acc[category.name].length + 1}`
        };
        acc[category.name].push(bgWithCover);
      });
    } else {
      if (!acc['未分类']) {
        acc['未分类'] = [];
      }
      const bgWithCover = {
        ...bg,
        cover: bg.cover || bg.url,
        name: bg.name || `背景${acc['未分类'].length + 1}`
      };
      acc['未分类'].push(bgWithCover);
    }
    return acc;
  }, {});
  
